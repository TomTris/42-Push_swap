#include "ft_swap.h"

int	check_dup(int *a)
{
	int	i;
	int	k;

	i = 1;
	while (i <= a[0] - 1)
	{
		k = i + 1;
		while (k <= a[0])
			if (a[i] == a[k++])
				return (0);
		i++;
	}
	return (1);
}
